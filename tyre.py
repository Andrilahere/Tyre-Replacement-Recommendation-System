def predict_for_tyre(tyre_id, top_k=5):
    tyre_id = str(tyre_id)
    candidates = df_pairs[df_pairs["tyre_id"] == tyre_id].copy()

    if candidates.empty:
        # Show available IDs to help debug
        available = df_pairs["tyre_id"].unique()[:10]
        return {"error": f"tyre_id '{tyre_id}' not found. Sample available IDs: {list(available)}"}

    candidates["predicted_score"] = model.predict(candidates[FEATURES])
    candidates = candidates.sort_values("predicted_score", ascending=False).head(top_k)

    recommendations = []
    for _, row in candidates.iterrows():
        reasons = []
        if row["nsd_diff"] <= 1:   reasons.append("closest NSD")
        if row["model_match"]:     reasons.append("same model")
        if row["brand_match"]:     reasons.append("same brand")
        if row["pattern_match"]:   reasons.append("same pattern")
        if not reasons:            reasons.append("good NSD match")

        recommendations.append({
            "reusable_tyre_id": str(int(row["reusable_id"])),
            "score":            round(float(row["predicted_score"]), 4),
            "reason":           " + ".join(reasons)
        })

    meta = candidates.iloc[0]
    return {
        "tyre_id":         tyre_id,
        "vehicle_id":      str(meta["vehicle_id"]),
        "position":        str(meta["position"]),
        "recommendations": recommendations
    }

