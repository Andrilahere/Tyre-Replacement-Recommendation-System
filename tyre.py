rows = []

for _, worn in df_master.iterrows():
    worn_nsd   = worn["avg_nsd"]
    worn_brand = worn["brand"]
    worn_model = worn["model"]
    worn_pat   = worn["pattern"]
    worn_size  = worn["tyre_size"]

    for _, cand in df_reusable.iterrows():
        nsd_diff    = abs(worn_nsd - cand["avg_nsd"])
        nsd_sim     = 1 / (1 + nsd_diff)
        model_match = int(worn_model == cand["model"])
        brand_match = int(worn_brand == cand["brand"])
        pat_match   = int(worn_pat   == cand["pattern"])
        size_match  = int(worn_size  == cand["tyre_size"])


        km_diff     = abs(worn["tyre_km"] - cand["tyre_km"]) if pd.notna(cand["tyre_km"]) else 9999
        km_sim      = 1 / (1 + km_diff / 10000)

        nsd_headroom = cand["avg_nsd"] - worn_nsd

        # Scored label (strict priority order respected by weights)
        score = (nsd_sim    * 0.40 +
                 model_match * 0.30 +
                 brand_match * 0.20 +
                 pat_match   * 0.10)

        rows.append({
            # IDs
            "tyre_id":          worn["tyre_id"],
            "vehicle_id":       worn["vehicle_id"],
            "position":         worn["position"],
            "reusable_id":      cand["reusable_id"],
            # Features
            "nsd_similarity":   round(nsd_sim, 4),
            "nsd_diff":         round(nsd_diff, 2),
            "nsd_headroom":     round(nsd_headroom, 2),
            "model_match":      model_match,
            "brand_match":      brand_match,
            "pattern_match":    pat_match,
            "size_match":       size_match,
            "km_similarity":    round(km_sim, 4),
            "worn_nsd":         worn_nsd,
            "candidate_nsd":    cand["avg_nsd"],
            "worn_km":          worn["tyre_km"],
            "candidate_km":     cand["tyre_km"],
            # Target
            "score":            round(score, 4)
        })

