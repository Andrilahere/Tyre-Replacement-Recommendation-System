import pandas as pd
import numpy as np
from sklearn.model_selection import GroupShuffleSplit

df = pd.read_csv("pairwise_dataset.csv")

df["nsd_score"] = np.exp(-0.8 * df["nsd_diff"])
# nsd_diff=0   → nsd_score=1.0  (perfect match)
# nsd_diff=0.5 → nsd_score=0.37
# nsd_diff=1   → nsd_score=0.14
# nsd_diff=2   → nsd_score=0.02 (almost worthless)

df["tiebreaker"] = (
    df["model_match"]   * 0.20 +
    df["brand_match"]   * 0.12 +
    df["pattern_match"] * 0.05 +
    df["size_match"]    * 0.03
)

# tiebreaker fills remaining 0.30 only when NSD is already good
df["score"] = df["nsd_score"] * 0.65 + df["tiebreaker"] * 0.35

# Normalise to 0–1
df["score"] = (df["score"] - df["score"].min()) / (df["score"].max() - df["score"].min())

df["nsd_x_model"]   = df["nsd_similarity"] * df["model_match"]   * 3.0
df["nsd_x_brand"]   = df["nsd_similarity"] * df["brand_match"]   * 2.0
df["nsd_x_pattern"] = df["nsd_similarity"] * df["pattern_match"] * 1.0


print("Score distribution:")
print(df["score"].describe().round(4))
print(f"\nCorrelation of features with score:")
for f in ["nsd_similarity","nsd_diff","model_match","brand_match","pattern_match"]:
    print(f"  {f:20s}: {df[f].corr(df['score']):+.4f}")


